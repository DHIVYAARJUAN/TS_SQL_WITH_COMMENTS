DROP TRIGGER IF EXISTS TRG_TS_ADMIN_WEEKLY_REPORT_DETAILS_UPDATE;
CREATE TRIGGER TRG_TS_ADMIN_WEEKLY_REPORT_DETAILS_UPDATE  
AFTER UPDATE ON ADMIN_WEEKLY_REPORT_DETAILS
FOR EACH ROW
BEGIN 
	
	DECLARE OLD_VALUE TEXT DEFAULT '';
	DECLARE NEW_VALUE TEXT DEFAULT '';
	
	IF ((OLD.AWRD_ID!= NEW.AWRD_ID) OR (OLD.AWRD_REPORT!= NEW.AWRD_REPORT) OR (OLD.AWRD_DATE!= NEW.AWRD_DATE)) THEN
		SET OLD_VALUE = CONCAT(OLD_VALUE,'AWRD_ID=', OLD.AWRD_ID,','); 
	END IF;
	
	IF (OLD.AWRD_REPORT!= NEW.AWRD_REPORT) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'AWRD_REPORT=', OLD.AWRD_REPORT,','); 
	END IF;
	IF (OLD.AWRD_REPORT!= NEW.AWRD_REPORT) THEN   
		SET NEW_VALUE = CONCAT(NEW_VALUE,'AWRD_REPORT=', NEW.AWRD_REPORT,','); 
	END IF;
	
	IF (OLD.AWRD_DATE!= NEW.AWRD_DATE) THEN  
		SET OLD_VALUE = CONCAT(OLD_VALUE,'AWRD_DATE=', OLD.AWRD_DATE,','); 
	END IF;
	IF (OLD.AWRD_DATE!= NEW.AWRD_DATE) THEN  
		SET NEW_VALUE = CONCAT(NEW_VALUE,'AWRD_DATE=', NEW.AWRD_DATE,','); 
	END IF;
	
	IF ((OLD.AWRD_ID!= NEW.AWRD_ID) OR (OLD.AWRD_REPORT!= NEW.AWRD_REPORT) OR (OLD.AWRD_DATE!= NEW.AWRD_DATE)) THEN
		IF (OLD.ULD_ID!= NEW.ULD_ID) THEN SET 
			OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
		END IF;
		IF (OLD.AWRD_TIMESTAMP!= NEW.AWRD_TIMESTAMP) THEN SET 
			OLD_VALUE = CONCAT(OLD_VALUE,'AWRD_TIMESTAMP=', OLD.AWRD_TIMESTAMP,','); 
		END IF;
	END IF;
	
	IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN
		IF(OLD_VALUE!=NEW_VALUE)THEN
			SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
			SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);
			INSERT INTO TICKLER_HISTORY(TP_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE,TH_USERSTAMP_ID)VALUES
			((SELECT TP_ID FROM TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
			(SELECT TTIP_ID FROM TICKLER_TABID_PROFILE WHERE TTIP_DATA='ADMIN_WEEKLY_REPORT_DETAILS'),OLD_VALUE,NEW_VALUE,
			(SELECT ULD_ID FROM ADMIN_WEEKLY_REPORT_DETAILS WHERE AWRD_ID=NEW.AWRD_ID));
		END IF;
	END IF;
END;