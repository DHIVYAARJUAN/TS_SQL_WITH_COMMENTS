DROP TRIGGER IF EXISTS TRG_TS_ONDUTY_ENTRY_DETAILS_UPDATE;
CREATE TRIGGER TRG_TS_ONDUTY_ENTRY_DETAILS_UPDATE  
AFTER UPDATE ON ONDUTY_ENTRY_DETAILS
FOR EACH ROW
BEGIN 
	
	DECLARE OLD_VALUE TEXT DEFAULT '';
	DECLARE NEW_VALUE TEXT DEFAULT '';
	
	IF ((OLD.OED_ID!= NEW.OED_ID) OR (OLD.OED_DATE!= NEW.OED_DATE) OR (OLD.OED_DESCRIPTION!= NEW.OED_DESCRIPTION)) THEN
		SET OLD_VALUE = CONCAT(OLD_VALUE,'OED_ID=', OLD.OED_ID,','); 
	END IF;
	IF (OLD.OED_DATE!= NEW.OED_DATE) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'OED_DATE=', OLD.OED_DATE,','); 
	END IF;
	IF (OLD.OED_DATE!= NEW.OED_DATE) THEN   
		SET NEW_VALUE = CONCAT(NEW_VALUE,'OED_DATE=', NEW.OED_DATE,','); 
	END IF;
	
	IF (OLD.OED_DESCRIPTION!= NEW.OED_DESCRIPTION) THEN  
		SET OLD_VALUE = CONCAT(OLD_VALUE,'OED_DESCRIPTION=', OLD.OED_DESCRIPTION,','); 
	END IF;
	IF (OLD.OED_DESCRIPTION!= NEW.OED_DESCRIPTION) THEN  
		SET NEW_VALUE = CONCAT(NEW_VALUE,'OED_DESCRIPTION=', NEW.OED_DESCRIPTION,','); 
	END IF;
	
	IF ((OLD.OED_ID!= NEW.OED_ID) OR (OLD.OED_DATE!= NEW.OED_DATE) OR (OLD.OED_DESCRIPTION!= NEW.OED_DESCRIPTION)) THEN
		IF (OLD.ULD_ID!= NEW.ULD_ID) THEN SET 
			OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
		END IF;
		IF (OLD.OED_TIMESTAMP!= NEW.OED_TIMESTAMP) THEN SET 
			OLD_VALUE = CONCAT(OLD_VALUE,'OED_TIMESTAMP=', OLD.OED_TIMESTAMP,','); 
		END IF;
	END IF;
	
	IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN
		IF(OLD_VALUE!=NEW_VALUE)THEN
			SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
			SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);
			INSERT INTO TICKLER_HISTORY(TP_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE,TH_USERSTAMP_ID)VALUES
			((SELECT TP_ID FROM TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
			(SELECT TTIP_ID FROM TICKLER_TABID_PROFILE WHERE TTIP_DATA='ONDUTY_ENTRY_DETAILS'),OLD_VALUE,NEW_VALUE,
			(SELECT ULD_ID FROM ONDUTY_ENTRY_DETAILS WHERE OED_ID=NEW.OED_ID));
		END IF;
	END IF;
END;