DROP TRIGGER IF EXISTS TRG_TS_PROJECT_DETAILS_UPDATE;
CREATE TRIGGER TRG_TS_PROJECT_DETAILS_UPDATE  
AFTER UPDATE ON PROJECT_DETAILS
FOR EACH ROW
BEGIN 
	
	DECLARE OLD_VALUE TEXT DEFAULT '';
	DECLARE NEW_VALUE TEXT DEFAULT '';
	
	IF ((OLD.PD_ID!= NEW.PD_ID) OR (OLD.PD_PROJECT_NAME!= NEW.PD_PROJECT_NAME) OR (OLD.PD_PROJECT_DESCRIPTION!= NEW.PD_PROJECT_DESCRIPTION)) THEN
		SET OLD_VALUE = CONCAT(OLD_VALUE,'PD_ID=', OLD.PD_ID,','); 
	END IF;
	
	IF (OLD.PD_PROJECT_NAME!= NEW.PD_PROJECT_NAME) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'PD_PROJECT_NAME=', OLD.PD_PROJECT_NAME,','); 
	END IF;
	IF (OLD.PD_PROJECT_NAME!= NEW.PD_PROJECT_NAME) THEN   
		SET NEW_VALUE = CONCAT(NEW_VALUE,'PD_PROJECT_NAME=', NEW.PD_PROJECT_NAME,','); 
	END IF;
	
	IF (OLD.PD_PROJECT_DESCRIPTION!= NEW.PD_PROJECT_DESCRIPTION) THEN  
		SET OLD_VALUE = CONCAT(OLD_VALUE,'PD_PROJECT_DESCRIPTION=', OLD.PD_PROJECT_DESCRIPTION,','); 
	END IF;
	IF (OLD.PD_PROJECT_DESCRIPTION!= NEW.PD_PROJECT_DESCRIPTION) THEN  
		SET NEW_VALUE = CONCAT(NEW_VALUE,'PD_PROJECT_DESCRIPTION=', NEW.PD_PROJECT_DESCRIPTION,','); 
	END IF;
	
	IF ((OLD.PD_ID!= NEW.PD_ID) OR (OLD.PD_PROJECT_NAME!= NEW.PD_PROJECT_NAME) OR (OLD.PD_PROJECT_DESCRIPTION!= NEW.PD_PROJECT_DESCRIPTION)) THEN
		IF (OLD.ULD_ID!= NEW.ULD_ID) THEN SET 
			OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
		END IF;
		IF (OLD.PD_TIMESTAMP!= NEW.PD_TIMESTAMP) THEN SET 
			OLD_VALUE = CONCAT(OLD_VALUE,'PD_TIMESTAMP=', OLD.PD_TIMESTAMP,','); 
		END IF;
	END IF;
	
	IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN
		IF(OLD_VALUE!=NEW_VALUE)THEN
			SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
			SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);
			INSERT INTO TICKLER_HISTORY(TP_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE,TH_USERSTAMP_ID)VALUES
			((SELECT TP_ID FROM TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
			(SELECT TTIP_ID FROM TICKLER_TABID_PROFILE WHERE TTIP_DATA='PROJECT_DETAILS'),OLD_VALUE,NEW_VALUE,
			(SELECT ULD_ID FROM PROJECT_DETAILS WHERE PD_ID=NEW.PD_ID));
		END IF;
	END IF;
END;