DROP PROCEDURE IF EXISTS SP_ACCESS_RIGHTS_TERMINATE_REJOIN_LOGINID;
CREATE PROCEDURE SP_ACCESS_RIGHTS_TERMINATE_REJOIN_LOGINID(IN DESTINATIONSCHEMA TEXT)
BEGIN
	DECLARE EXIT HANDLER FOR SQLEXCEPTION
	BEGIN 
		ROLLBACK;
	END;
	START TRANSACTION;
	SET @SET_SUB_ACCESS_VIEW=(SELECT CONCAT('CREATE OR REPLACE VIEW ',DESTINATIONSCHEMA,'.VW_SUB_ACCESS_RIGHTS_MAXRECVER AS SELECT ULD_ID,MAX(UA_REC_VER) AS RECVER 
	FROM ',DESTINATIONSCHEMA,'.USER_ACCESS GROUP BY ULD_ID'));
	PREPARE SET_SUB_ACCESS_VIEW_STMT FROM @SET_SUB_ACCESS_VIEW;
	EXECUTE SET_SUB_ACCESS_VIEW_STMT;
	SET @SET_ACCESS_RIGHTS_TERMINATE=(SELECT CONCAT('CREATE OR REPLACE VIEW ',DESTINATIONSCHEMA,'.VW_ACCESS_RIGHTS_TERMINATE_LOGINID AS
	SELECT ULD.ULD_LOGINID,URCN.URC_ID,URCN.URC_DATA,UA.UA_JOIN_DATE FROM ',DESTINATIONSCHEMA,'.USER_LOGIN_DETAILS ULD,',DESTINATIONSCHEMA,'.USER_ACCESS UA,',DESTINATIONSCHEMA,'.VW_SUB_ACCESS_RIGHTS_MAXRECVER V,',DESTINATIONSCHEMA,'.USER_RIGHTS_CONFIGURATION URCN,',DESTINATIONSCHEMA,'.ROLE_CREATION RC 
	WHERE ULD.ULD_ID=UA.ULD_ID AND UA.UA_REC_VER=V.RECVER AND V.ULD_ID=UA.ULD_ID AND V.ULD_ID=ULD.ULD_ID AND 
	UA.RC_ID=RC.RC_ID AND RC.URC_ID=URCN.URC_ID AND UA.UA_TERMINATE IS NULL GROUP BY ULD.ULD_ID'));
	PREPARE SET_ACCESS_RIGHTS_TERMINATE_STMT FROM @SET_ACCESS_RIGHTS_TERMINATE;
	EXECUTE SET_ACCESS_RIGHTS_TERMINATE_STMT;
	SET @SET_ACCESS_RIGHTS_REJOIN=(SELECT CONCAT('CREATE OR REPLACE VIEW ',DESTINATIONSCHEMA,'.VW_ACCESS_RIGHTS_REJOIN_LOGINID AS
	SELECT ULD.ULD_LOGINID,UA.UA_JOIN_DATE FROM ',DESTINATIONSCHEMA,'.USER_LOGIN_DETAILS ULD,',DESTINATIONSCHEMA,'.USER_ACCESS UA,',DESTINATIONSCHEMA,'.VW_SUB_ACCESS_RIGHTS_MAXRECVER V 
	WHERE ULD.ULD_ID=UA.ULD_ID AND UA.UA_REC_VER=V.RECVER AND V.ULD_ID=UA.ULD_ID AND V.ULD_ID=ULD.ULD_ID AND 
	UA.UA_TERMINATE IS NOT NULL GROUP BY ULD.ULD_ID'));
	PREPARE SET_ACCESS_RIGHTS_REJOIN_STMT FROM @SET_ACCESS_RIGHTS_REJOIN;
	EXECUTE SET_ACCESS_RIGHTS_REJOIN_STMT;
	COMMIT;
END;
